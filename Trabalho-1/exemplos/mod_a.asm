MOD_A:    BEGIN
PUBLIC    DATA1
PUBLIC    DATA2
PUBLIC    DATA3
PUBLIC    DATA4
PUBLIC    DOIS
PUBLIC    DEZ
PUBLIC	  RET_MOD_B	
PUBLIC	  RET_MOD_C	
PUBLIC	  RET_MOD_D	
EXTERN: MOD_B
EXTERN: MOD_C
EXTERN: MOD_D
SECTION TEXT
INPUT     DATA1
