RET_MOD_C: JMP MOD_D
RET_MOD_D: STOP
SECTION DATA
DOIS: CONST 2
DEZ: CONST 0x0A
DATA1: SPACE
DATA2: SPACE
DATA3: SPACE
DATA4: SPACE


END
